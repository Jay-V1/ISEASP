                            <span class="label label-<?php echo $payment_status_class; ?>">
                                <?php echo $payment_status_text; ?>
                            </span>
                            <?php if($scholar->payment_date): ?>
                            <br><small><?php echo date('M d, Y', strtotime($scholar->payment_date)); ?></small>
                            <?php endif; ?>
                        </td>
                        <td class="text-center">
                            <?php if($scholar->payment_status != 'paid' && $payroll->status == 'approved'): ?>
                            <a href="controller.php?action=mark_stipend_paid&detail_id=<?php echo $scholar->id; ?>&payroll_id=<?php echo $payroll_id; ?>" 
                               class="btn btn-success btn-xs" onclick="return confirm('Mark stipend for <?php echo $scholar->FULLNAME; ?> as paid?')">
                                <i class="fa fa-check"></i> Mark Paid
                            </a>
                            <?php endif; ?>
                            <?php if($scholar->payment_status == 'paid'): ?>
                            <button class="btn btn-default btn-xs" disabled>
