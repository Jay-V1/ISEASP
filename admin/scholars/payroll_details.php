                    </tr>
                    <?php endforeach; ?>
                    
                    <?php if(empty($scholars)): ?>
                    <tr>
                        <td colspan="8" class="text-center">No scholars found for this payroll.</td>
                    </tr>
                    <?php endif; ?>
                </tbody>
                <tfoot>
                    <tr class="bg-gray" style="font-weight: bold;">
                        <td colspan="5" class="text-right"><strong>TOTAL:</strong></td>
